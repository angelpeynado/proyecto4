    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        SqlUsuarios sqLUsarios = new SqlUsuarios();
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        if(jTable2.getSelectedRow() != -1) {
            Integer id = (Integer)model.getValueAt(jTable2.getSelectedRow(), 0);
            Usuario usuario = new Usuario();
            usuario.setId((int)id);
            sqLUsarios.eliminarUsuario(usuario);
        }
        cargarUsuarios();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
         if(jTable2.getSelectedRow() != -1) {
            Usuario usuario = new Usuario();
            int filaSeleccionada = jTable2.getSelectedRow();
            usuario.setId((int)model.getValueAt(filaSeleccionada, 0));
            usuario.setUsuario((String)model.getValueAt(filaSeleccionada, 1));
            usuario.setNombre((String)model.getValueAt(filaSeleccionada, 2));
            usuario.setApellido((String)model.getValueAt(filaSeleccionada, 3));
            usuario.setTelefono((String)model.getValueAt(filaSeleccionada, 4));
            usuario.setCorreo((String)model.getValueAt(filaSeleccionada, 5));
            Actualizacion actualizacionForm = new Actualizacion();
            actualizacionForm.setUsuario(usuario);
            actualizacionForm.setVisible(true);
            this.setVisible(false);
            this.dispose();
         }
    }//GEN-LAST:event_jButton2ActionPerformed

