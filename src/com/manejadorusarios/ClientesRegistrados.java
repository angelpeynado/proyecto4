    private void cargarUsuarios() {
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        model.setRowCount(0);
        SqlUsuarios sqlUsuarios = new SqlUsuarios();
        ArrayList lista = sqlUsuarios.listaUsuarios();
        
        for(Object elemento : lista) {
            Usuario usuario = (Usuario) elemento;
            Object[] values = {
                usuario.getId(),  usuario.getUsuario(), usuario.getNombre(),
                usuario.getApellido(), usuario.getTelefono(), usuario.getCorreo()
            };
            model.addRow(values);
        }
    }

